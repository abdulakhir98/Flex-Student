    private void subject1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_subject1ActionPerformed
        sec1.removeAllItems();
        int count = 0;
        int i = 0;
        String a = (String) subject1.getSelectedItem();
        try {
            while(!ad.getcourses().get(count).getname().equals(a)){
                count++;
            }
            ResultSet rs;
            rs = db.getsections(a);
            while (rs.next()) {
                sec1.addItem(rs.getString("section"));
            }
        }
        catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_subject1ActionPerformed

