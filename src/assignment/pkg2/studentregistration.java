    public void BindCombo(){
        try {
            int i = 0;
            String temp = null;
            int csize = ad.getcourses().size();
            while (i < csize) {
                if(!(ad.getcourses().get(i).getname()).equals(temp)){
                    subject1.addItem(ad.getcourses().get(i).getname());
                    subject2.addItem(ad.getcourses().get(i).getname());
                    subject3.addItem(ad.getcourses().get(i).getname());
                    temp = ad.getcourses().get(i).getname();
                }
                i++;
            }
        }
        catch(Exception e){
             System.out.println(e);
        }
        
    }

